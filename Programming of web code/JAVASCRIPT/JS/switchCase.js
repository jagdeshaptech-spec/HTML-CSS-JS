let age = prompt("Enter your age:");

switch(age){
    case "18":
        document.write("You are 18 years old.");
        break;
    case "19":
        document.write("You are 19 years old.");
        break;  
    case "20":
        document.write("You are 20 years old.");
        break;
    case "21":
        document.write("You are 21 years old.");
        break;
    case "22":
        document.write("You are 22 years old.");
        break;
    case "23":
        document.write("You are 23 years old.");
        break;
    default:    
        document.write("Age not matched.");
}